public class relationalopt {
    public static void main(String[] args) {
        //< less than
        //> greater than 
        // == equal to
        // != not equal to
        // <= less than or equal to
        // >= greater than or equal to
        int x = 7;
        int y = 5;
        
        double a = 7.4;
        float b = 7.4f;

        boolean result = x <= y;
        System.out.println(result);
        boolean result2 = a == b;
        System.out.println(result2);
    }
}
